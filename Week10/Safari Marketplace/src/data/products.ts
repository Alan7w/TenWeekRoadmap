export const getProductsByCategory = (category: Product['category']): Product[] => {
  return mockProducts.filter(product => product.category === category);
};

export const getFeaturedProducts = (): Product[] => {
  return mockProducts.filter(product => product.featured);
};

export const getProductById = (id: string): Product | undefined => {
  return mockProducts.find(product => product.id === id);
};

export const getSubcategoriesByCategory = (category: Product['category']): string[] => {
  const subcategories = mockProducts
    .filter(product => product.category === category)
    .map(product => product.subcategory);
  return [...new Set(subcategories)];
};

export const getSizesByCategory = (category: Product['category']): string[] => {
  const sizes = mockProducts
    .filter(product => product.category === category)
    .flatMap(product => product.sizes);
  return [...new Set(sizes)];
};

export const getColorsByCategory = (category: Product['category']): string[] => {
  const colors = mockProducts
    .filter(product => product.category === category)
    .flatMap(product => product.colors);
  return [...new Set(colors)];
};

export const getPriceRangeByCategory = (category: Product['category']): { min: number; max: number } => {
  const categoryProducts = mockProducts.filter(product => product.category === category);
  const prices = categoryProducts.map(product => product.price);
  return {
    min: Math.min(...prices),
    max: Math.max(...prices),
  };
};